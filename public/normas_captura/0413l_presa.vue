		<h2>Normas de captura</h2>
<p>Como norma general las presas se capturan como <strong>elementos lineales cerrados</strong> (coordenadas inicial y final coincidentes) representados por la línea de su contorno cuando su anchura (teniendo en cuenta la caída) sea <strong>igual o mayor de 10m</strong>, y como una línea no cerrada que representa el eje longitudinal de la construcción cuando la presa tenga una anchura inferior a 10m. La presa debe ser <strong>coincidente con la línea de agua correspondiente</strong>. Todas las presas catalogadas deben llevar siempre su <strong>cota de coronación</strong> de acuerdo al inventario.</p>

<p>En el caso de los <strong>aliviaderos</strong> dentro de la presa se registran también como <strong>parte del contorno</strong> de la presa.</p>

<p>Los <strong>diques de contención en balsas o estanques</strong> se capturan también como presas, siendo estas generalmente de materiales sueltos.</p>

