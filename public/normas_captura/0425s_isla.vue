		<h2>Normas de captura</h2>
<p>Como norma general las entidades que se capturan para este objeto geográfico son elementos superficiales y representan el límite de separación entre la zona terrestre y la fluvial o marítima, existente en el instante de la captura. Su altura (Z) debe ser constante. Estos objetos deben capturarse en sentido horario.</p>

<p>En el caso de las <b>islas marítimas</b> se capturan a cota cero y coincidente su contorno con línea de costa y curva de nivel de cota cero.</p>

<p>Las <b>islas en aguas interiores</b> deben crear un hueco sobre el recinto de la lámina de agua en el que se encuentran. El borde de este hueco será coincidente con el borde de la isla sin que existan espacios en blanco.</p>

<p>Criterio de selección</p>
<ul>
	<li><b>Marítimas:</b> Deben capturarse todas, así como los islotes, escollos, farallones, bajos, etc. sea cual sea su superficie.</li>
	<li><b>Aguas interiores:</b> Deben capturarse todas cuya superficie sea mayor de 100 m2. En el caso de las que pudieran estar presentes en el interior de los embalses, solo deben tener representación aquellas cuya altitud sea superior a la cota de la línea de máximo embalse.</li>
</ul>

